package slice

// Chunkify splits a slice into chunks of the specified size.
// The last chunk may have a smaller size if the slice length is not divisible by the chunk size.
// This function does not modify the original slice.
// The function returns a new slice containing the chunks.
func Chunkify[T any](slice []T, chunkSize int) [][]T {
	var chunks [][]T
	for {
		if len(slice) == 0 {
			break
		}

		// required to avoid slicing method beyond slice capacity
		if len(slice) < chunkSize {
			chunkSize = len(slice)
		}
		chunks = append(chunks, slice[0:chunkSize])
		slice = slice[chunkSize:]
	}
	return chunks
}
